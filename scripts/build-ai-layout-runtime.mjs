import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(SCRIPT_DIR, '..');
const SKILLS_ROOT = path.join(ROOT, 'ai-layout-skills');
const OUTPUT_FILE = path.join(ROOT, 'services', 'ai-layout-runtime', 'generated-skills.js');
const CHECK_MODE = process.argv.includes('--check');

const SHARED_FILES = {
  colorPalettes: path.join(SKILLS_ROOT, '_shared', 'assets', 'color-palettes.json'),
  blockCatalog: path.join(SKILLS_ROOT, '_shared', 'assets', 'block-catalog.json'),
  wechatSafeStylePrimitives: path.join(SKILLS_ROOT, '_shared', 'assets', 'wechat-safe-style-primitives.json'),
  schema: path.join(SKILLS_ROOT, '_shared', 'schema', 'article-layout.schema.json'),
  template: path.join(SKILLS_ROOT, '_shared', 'templates', 'article-layout.template.json'),
};

function assertExists(filePath, label = filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing required AI layout runtime resource: ${label}`);
  }
}

function readJson(filePath) {
  assertExists(filePath, filePath);
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function readText(filePath) {
  assertExists(filePath, filePath);
  return fs.readFileSync(filePath, 'utf8').replace(/\r\n/g, '\n').trimEnd();
}

function listSkillDirectories() {
  assertExists(SKILLS_ROOT, SKILLS_ROOT);
  return fs.readdirSync(SKILLS_ROOT, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && !entry.name.startsWith('.') && entry.name !== '_shared')
    .map((entry) => entry.name)
    .sort((left, right) => left.localeCompare(right, 'zh-Hans-CN'));
}

function loadExamples(skillDir) {
  const examplesDir = path.join(skillDir, 'examples');
  assertExists(examplesDir, examplesDir);
  return fs.readdirSync(examplesDir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith('.json'))
    .map((entry) => entry.name)
    .sort((left, right) => left.localeCompare(right, 'zh-Hans-CN'))
    .map((fileName) => ({
      name: fileName,
      value: readJson(path.join(examplesDir, fileName)),
    }));
}

function compareSkills(left, right) {
  const leftOrder = Number(left?.manifest?.order || 999);
  const rightOrder = Number(right?.manifest?.order || 999);
  if (leftOrder !== rightOrder) return leftOrder - rightOrder;
  return String(left?.manifest?.label || left?.id).localeCompare(String(right?.manifest?.label || right?.id), 'zh-Hans-CN');
}

function loadSkill(skillId) {
  const skillDir = path.join(SKILLS_ROOT, skillId);
  const manifest = readJson(path.join(skillDir, 'manifest.json'));
  const blocks = readJson(path.join(skillDir, 'blocks.json'));
  const fallback = readJson(path.join(skillDir, 'fallback.json'));
  const prompt = readText(path.join(skillDir, 'prompt.md'));
  const skillDoc = readText(path.join(skillDir, 'SKILL.md'));
  const examples = loadExamples(skillDir);

  return {
    id: manifest.id,
    manifest,
    prompt,
    blocks,
    fallback,
    skillDoc,
    examples,
  };
}

function buildRegistryData() {
  const shared = Object.fromEntries(
    Object.entries(SHARED_FILES).map(([key, filePath]) => [key, readJson(filePath)])
  );
  shared.version = shared.wechatSafeStylePrimitives?.version || shared.colorPalettes?.version || '2026.03.25-alpha.1';

  const skills = listSkillDirectories()
    .map((skillId) => loadSkill(skillId))
    .sort(compareSkills);

  return { shared, skills };
}

function buildOutput(registryData) {
  return `// Auto-generated by scripts/build-ai-layout-runtime.mjs. Do not edit manually.
module.exports = ${JSON.stringify(registryData, null, 2)};
`;
}

function main() {
  const content = buildOutput(buildRegistryData());
  const existing = fs.existsSync(OUTPUT_FILE)
    ? fs.readFileSync(OUTPUT_FILE, 'utf8')
    : null;

  if (CHECK_MODE) {
    if (existing !== content) {
      console.error('[build-ai-layout-runtime] generated output is out of sync.');
      console.error('[build-ai-layout-runtime] run: npm run generate:ai-layout-runtime');
      process.exit(1);
    }
    console.log('[build-ai-layout-runtime] ai layout runtime output is up to date.');
    return;
  }

  if (existing === content) {
    console.log('[build-ai-layout-runtime] no changes.');
    return;
  }

  fs.writeFileSync(OUTPUT_FILE, content, 'utf8');
  console.log(`[build-ai-layout-runtime] wrote ${OUTPUT_FILE}`);
}

main();
